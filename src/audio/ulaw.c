void ulaw2pcm(const uint8_t* ulawData, int16_t* pcmData, int numSamples)
{
    for (int i = 0; i < numSamples; ++i)
        pcmData[i] = pcm_of_ulaw[ulawData[i]];
}

// This function can be replaced with a table of all pcm values (65536)
static uint8_t ulaw_of_pcm(int16_t pcmValue) 
{
   const uint16_t BIAS = 0x84;
   const uint16_t CLIP = 32635;
   uint16_t mask = 0x4000;
   uint8_t sign = 0;
   uint8_t exponent = 14;
   uint8_t mantisa = 0;
   if (pcmValue < 0)
   {
      pcmValue = -pcmValue;
      sign = 0x80;
   }
   pcmValue += BIAS;
   if (pcmValue > CLIP)
      pcmValue = CLIP;

   while ((pcmValue & mask) != mask && exponent >= 7) {
      mask >>= 1;
      exponent--;
   }        
   mantisa = (pcmValue >> (exponent - 4)) & 0x0f;
   return (~(sign | ((exponent - 7) << 4) | mantisa));
}

void pcm2ulaw(const int16_t* pcmData, uint8_t* ulawData,  int numSamples)
{
    for (int i = 0; i < numSamples; ++i)
        ulawData[i] = ulaw_of_pcm(pcmData[i]);
}
